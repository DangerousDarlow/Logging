
namespace Logging
{
  public enum LogLevel
  {
    /// <summary>
    /// An error has occurred
    /// </summary>
    Error,

    /// <summary>
    /// An error may have occurred or be likely to occur
    /// </summary>
    Warning,

    /// <summary>
    /// Something significant has happened
    /// </summary>
    Info,

    /// <summary>
    /// Something that might assist debugging has happened
    /// </summary>
    Debug
  }
}
